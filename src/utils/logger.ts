// Frontend logger — suppresses output in production builds
// @ts-ignore - Vite env typing
const isDev = !import.meta.env?.PROD;

export const logger = {
  log: (...args: unknown[]) => {
    if (isDev) console.log(...args);
  },
  warn: (...args: unknown[]) => {
    if (isDev) console.warn(...args);
  },
  error: (...args: unknown[]) => {
    if (isDev) console.error(...args);
  },
};
